    final void getUpdateFromDatabase(){
        try{
            Class.forName("org.mariadb.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/faculty", "user", "pass");
            Statement st = con.createStatement();
            String query = "SELECT FacultyID, Name, EmailID, PhoneNumber, Department, Post FROM faculty.facultyInfo WHERE FacultyID = '" + facultyID + "'; ";
            
            ResultSet resStr = st.executeQuery(query);
            
            while(resStr.next()){
                idDataLb.setText(resStr.getString(1));
                nameLb.setText(resStr.getString(2));
                emailLb.setText(resStr.getString(3));
                designationLb.setText(resStr.getString(6));
                departmentLb.setText(resStr.getString(5));
            }
            
            
            query = "Select * From faculty.Programs Where FacultyID = '" + facultyID + "'"
                    + "Order By TimeMonth And TimeYear";
            resStr = st.executeQuery(query);
            
            int counter = 0;
            Vector<Vector<Object>> rows = new Vector<>();
            
            while (resStr.next()) {
                Vector<Object> anotherRow = new Vector<>();
                anotherRow.add(++counter);
                anotherRow.add(resStr.getString("Title"));
                if(resStr.getString("Direction").equals("Attended"))
                    anotherRow.add("Attended " + resStr.getString("Description"));
                else
                    anotherRow.add("Conducted " + resStr.getString("Description"));
                anotherRow.add(resStr.getString("Type"));
                anotherRow.add(resStr.getString("TimeMonth") + "/" + resStr.getString("TimeYear"));
                rows.add(anotherRow);
            }
            
            Vector<Object> cols = new Vector<>();
            cols.add("Sr. No.");
            cols.add("Title");
            cols.add("Description");
            cols.add("Program Type");
            cols.add("Time");
            
            JTable dataTable = new JTable(rows, cols);
            
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);
            dataTable.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
            dataTable.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);
            dataTable.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
            
            TableColumn column = dataTable.getColumnModel().getColumn(0);
            column.setMaxWidth(30);
            column = dataTable.getColumnModel().getColumn(4);
            column.setMaxWidth(50);
            
            dataTable.setRowHeight(30);
            
            JScrollPane pane = new JScrollPane(dataTable);
            mainDataPanel.add(pane);
            
            con.close();
        }
        catch(Exception e){e.printStackTrace();}
    }
    
    
